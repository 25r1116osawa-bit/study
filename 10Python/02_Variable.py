# =============
# 変数とデータ型
# =============

# 整数型
print("--- 整数型 ---")
int01 = 123
print(int01)

# データ型の表示
print(type(int01))
print("10進数")
print(10)
print("2進数")
print(0b10)
print("8進数")
print(0o10)
print("16進数")
print(0x10)

print("-- 浮動小数点型 --")
float01 = 1.23
print(float01)
print(type(float01))

float02 = 1.2e-5
print(float02)
print(type(float02))

float03 = 5 / 3
print(float03)
print(type(float03))

print("-- 文字列型 --")
str01 = "abc"
str02 = 'def'

print(str01,str02)
print(type(str01),type(str02))

print("-- 論理値 --")
bool01 = True
bool02 = False

print(bool01,bool02)
print(type(bool01),type(bool02))